#include<stdio.h>
int main(){
	int a=0,b=0,c=0,d=0,s=0;
	//a为生命值,b为扣除生命值,c为朱雀技能扣血,d为人物回合回血,s为伤害减免值 
	printf("请输入你的生命值:"); 
	scanf("%d",&a);
	printf("请输入你的伤免值:");
	scanf("%d",&s);
	s=s>700?700:s;
	b=a*(s/100);
	printf("请输入人物回合回血值:");
	scanf("%d",&d);
	c=800;//22朱雀扣800血量
	for(int i=1; i<=30 ; i++){
		if(i==1){
			a=a-b;//霸天虎技能扣除30%最大血量 
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
			a=a+d-c;//回合回血和朱雀技能扣血 
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
		}
		if(i==2){
			a=a-b;//霸天虎技能扣除30%最大血量 
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
			a=a+d;//回合回血
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
			a=a-b-c;
			/*霸天虎技能扣除30%最大血量
			(第二回合有第一回合遗留效果) 
			c为朱雀技能血量扣除 
			*/ 
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
		}
		if(i>=3){
			a=a-b-b;
			/*霸天虎技能扣除30%最大血量 
				霸天虎技能持续3回合
				所以首先扣除了60%的血量
			*/ 
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
			a=a+d;//回合回血
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
			a=a-b-c;
			/*霸天虎技能扣除30%最大血量
			(第二回合有第一回合遗留效果) 
			c为朱雀技能血量扣除 
			*/ 
			if(a<=0){
				printf("你的血量:%d\n",a);
				printf("你已经挂了\n");
			}
		}
	}
 }
